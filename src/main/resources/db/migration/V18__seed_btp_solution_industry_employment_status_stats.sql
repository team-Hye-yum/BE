), normalized_ksic_divisions as (
    select distinct on (
        regexp_replace(replace(replace(trim(division_name), ' ', ''), ';', ''), '\([^)]*\)', '', 'g')
    )
        section_code,
        section_name,
        division_name,
        regexp_replace(replace(replace(trim(division_name), ' ', ''), ';', ''), '\([^)]*\)', '', 'g') as normalized_division_name
    from public.ksic_info
    where division_code is not null and division_name is not null and trim(division_name) <> ''
    order by regexp_replace(replace(replace(trim(division_name), ' ', ''), ';', ''), '\([^)]*\)', '', 'g'), division_code
), normalized_source_rows as (
    select
        source_rows.*,
        regexp_replace(replace(replace(trim(source_rows.middle_industry_name), ' ', ''), ';', ''), '\([^)]*\)', '', 'g') as normalized_middle_industry_name
    from source_rows
)
